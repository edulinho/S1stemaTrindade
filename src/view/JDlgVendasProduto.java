    private void jBtnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnConfirmarActionPerformed

        EtsVendasProduto etsVendasProduto = new EtsVendasProduto();
        etsVendasProduto.setEtsProduto((EtsProduto) jCboProduto.getSelectedItem() );
        etsVendasProduto.setEtsQuantidade(jTxtQuantidade.getText() );
        etsVendasProduto.setEtsValorUnitario(Util.strDouble(jTxtValorUnitario.getText()));
        if (getTitle().toUpperCase().substring(0, 1).equals("I")) {
            jDlgVendas.controle.addBean(etsVendasProduto);
        } else {
            jDlgVendas.controle.updateBean(jDlgVendas.getSelectedRowProd(), etsVendasProduto);
        }
        setVisible(false);

    }//GEN-LAST:event_jBtnConfirmarActionPerformed

